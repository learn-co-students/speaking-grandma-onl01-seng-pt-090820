    return "NO, NOT SINCE 1938!"
  end
  if phrase == "What did you eat today?".upcase
  return "NO, NOT SINCE 1938!"
  end
  if phrase == "Hi!"
    return "HUH?! SPEAK UP, SONNY!"
  end
end